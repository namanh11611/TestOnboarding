public class OnBoardData {
    public int imageId;
    public int titleId;
    public int descId;
    public String trackingKey;

    public OnBoardData(int imageId, int titleId, int descId, String trackingKey) {
        this.imageId = imageId;
        this.titleId = titleId;
        this.descId = descId;
        this.trackingKey = trackingKey;
    }
}
